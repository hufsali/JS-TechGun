var x=4.7;
console.log(x);

// -----------------------round off fn----------------------

var x=Math.round(4.6); //make it 5
console.log(x);

//------------------------high value fn---------------------

var x=Math.ceil(4.6); //gives the highest value single point upto 4 would be 5
console.log(x);

// -----------------------low value fn----------------------

var x=Math.floor(4.6); //gives the less value even more than .5 would be 4
console.log(x);

// ---------------------remove point fn---------------------

var x=Math.trunc(4.6); //will remove point and next value
console.log(x);

//---------------------power fn-----------------------------

var x=Math.pow(10 , 3); //two parameters base and power required
console.log(x);

// ------------------------square root fn-------------------

var x=Math.sqrt(121); 
console.log(x);

// ------------------------minimum fn-----------------------

var x=Math.min( 4 , 6 , 9 , 12 , 90 ); //sorts out the minimum value
console.log(x);

// ---------------------------Maximum fn--------------------

var x=Math.max( 4 , 6 , 9 , 12 , 90 ); //sorts out the maxmum value
console.log(x);

